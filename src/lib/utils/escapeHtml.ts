/**
 * Escape HTML special characters to prevent XSS in dynamic HTML strings.
 * Use this whenever interpolating user data into raw HTML templates.
 */
export function escapeHtml(str: string | null | undefined): string {
  if (!str) return '';
  return str
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;');
}
